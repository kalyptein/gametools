exports.cityName = (gender=undefined) => {
    if (!gender || (gender != 'male' && gender != 'female'))
        gender = (Math.floor(utils.m.random() * 2) == 0) ? 'male' : 'female'

    switch (gender.toLowerCase()) {
        case 'male': 
            return nameset_male_first.pick().description + nameset_male_second.pick().description + ' (male)'
            break
        case 'female': 
            return nameset_female.pick().description + ' (female)'
    }
}

